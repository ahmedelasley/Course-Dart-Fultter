/*

8. Write a Dart program that declares two numeric variables and performs addition, subtraction, multiplication, division, and modulo on them. Print each result.

*/

void main() {
  int num1, num2;
  num1 = 12;
  num2 = 5;

  print("number 1 + number 2 =  ${num1 + num2}");
  print("number 1 - number 2 =  ${num1 - num2}");
  print("number 1 * number 2 =  ${num1 * num2}");
  print("number 1 / number 2 =  ${num1 / num2}");
  print("number 1 % number 2 =  ${num1 % num2}");
}
